var all = JSON.parse(fs.readFileSync(ALL_PATH, 'utf8'));

// 日本語名→IDのマップ
var jaMap = {};
all.forEach(function(p) { jaMap[p.ja] = p.id; });

// 公式リストをIDに変換
var enabledIds = [];
var notFound = [];
OFFICIAL_LIST.forEach(function(ja) {
  var id = jaMap[ja];
  if (id !== undefined) {
    enabledIds.push(id);
  } else {
    notFound.push(ja);
  }
});

console.log('公式251匹 マッチ結果:');
console.log('  マッチ:', enabledIds.length, '匹');
if (notFound.length > 0) {
  console.log('  未マッチ:', notFound.join(', '));
}

// ランキングデータを生成
// TOP50はALTEMAの順位、残りは図鑑番号順に51位以降を割り当て
var rankMap = {};
ALTEMA_TOP50.forEach(function(ja, i) {
  var id = jaMap[ja];
  if (id !== undefined) rankMap[id] = i + 1;
});

// TOP50以外はrank=999（図鑑番号でソート）
var rankingData = {
  updatedAt: new Date().toISOString(),
  source: 'ALTEMA TOP50 (2026-04-10)',
  ranks: rankMap
};

fs.writeFileSync(RANK_PATH, JSON.stringify(rankingData, null, 2));
